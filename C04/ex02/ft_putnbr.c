#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	print;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		nb = nb * (-1);
		write(1, "-", 1);
	}
	if (nb < 10)
	{
		print = nb + '0';
		write(1, &print, 1);
	}
	else
	{
		ft_putnbr(nb / 10);
		print = nb % 10 + '0';
		write(1, &print, 1);
	}
}
