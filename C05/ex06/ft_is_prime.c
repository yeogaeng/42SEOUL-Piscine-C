int	ft_is_prime(int nb)
{
	int	i;

	if (nb <= 1)
		return (0);
	if (nb == 2 || nb == 3)
		return (1);
	i = 2;
	while (i <= (nb / 2))
	{
		if (nb % 2 == 0)
			return (0);
		else if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
